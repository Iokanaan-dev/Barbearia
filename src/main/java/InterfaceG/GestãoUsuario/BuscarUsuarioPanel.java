    private void jbuscarUsuario1ActionPerformed(java.awt.event.ActionEvent evt) {                                                
        String nome = jNomeUsuarioField.getText().trim();

        if (nome.isEmpty()) {
            carregarTodos();
            return;
        }

        try {
            // Assumindo que GestaoUsuarios tem 'buscarPorNome'
            List<Usuario> encontrados = GestaoUsuarios.getInstancia().buscarPorNome(nome); // <-- Alterado

            if (encontrados.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nenhum usuário encontrado!"); // <-- Alterado
                return;
            }

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            for (Usuario u : encontrados) { // <-- Alterado
                model.addRow(new Object[]{
                    u.getNome(),
                    u.getUsuario(),
                    u.getCpf(),
                    u.getTelefone(),
                    u.getTipoUsuario(),
                    u.getId()
                }); // <-- Linhas Alteradas
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao buscar usuário!"); // <-- Alterado
        }
    }                                               

    private void jExcluirUsuario3ActionPerformed(java.awt.event.ActionEvent evt) {                                                 
        int row = jTable1.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um usuário na tabela!"); // <-- Alterado
            return;
        }

        String id = jTable1.getValueAt(row, 5).toString(); // <-- Coluna do ID mudou para 5

        int confirm = JOptionPane.showConfirmDialog(this,
                "Tem certeza que deseja excluir este usuário?", // <-- Alterado
                "Confirmar",
                JOptionPane.YES_NO_OPTION);

        if (confirm != JOptionPane.YES_OPTION) return;

        try {
            GestaoUsuarios.getInstancia().remover(id); // <-- Alterado
            carregarTodos();
            JOptionPane.showMessageDialog(this, "Usuário excluído!"); // <-- Alterado
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao excluir usuário!"); // <-- Alterado
        }
    }                                                

    private void jEditarUsuario2ActionPerformed(java.awt.event.ActionEvent evt) {                                                
        int linha = jTable1.getSelectedRow();

        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um usuário!"); // <-- Alterado
            return;
        }

        String id = jTable1.getValueAt(linha, 5).toString(); // <-- Coluna do ID mudou para 5
        Usuario usuario = GestaoUsuarios.getInstancia().buscarPorId(id); // <-- Alterado

        EditarUsuarioPanel painel = new EditarUsuarioPanel(root); // <-- Alterado
        painel.setUsuario(usuario); // <-- Assumindo que EditarUsuarioPanel tem esse método

        root.setSubPainel(painel);
    }                                               


