    private void carregarTodos() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        List<Usuario> lista = GestaoUsuarios.getInstancia().getListaCopia(); // <-- Alterado

        for (Usuario u : lista) { // <-- Alterado
            model.addRow(new Object[]{
                u.getNome(),
                u.getUsuario(), // Assumindo que o login é 'getUsuario()'
                u.getCpf(),
                u.getTelefone(),
                u.getTipoUsuario(), // Assumindo que o tipo é 'getTipoUsuario()'
                u.getId()
            }); // <-- Linhas Alteradas
        }
    }    
    
