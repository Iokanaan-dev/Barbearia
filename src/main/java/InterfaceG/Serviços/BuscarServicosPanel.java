    private void jbuscarServico1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbuscarServico1ActionPerformed
    
 String nome = jNomeServicoField.getText().trim();

        if (nome.isEmpty()) {
            carregarTodos();
            return;
        }

        try {
            List<Servico> encontrados = GestaoServicos.getInstancia().buscarPorNome(nome);

            if (encontrados.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nenhum serviço encontrado!");
                return;
            }

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            for (Servico s : encontrados) {
                model.addRow(new Object[]{
                    s.getNome(),
                    s.getPreco(),
                    s.getDescricao(),
                    s.getTempoEmMinutos(),
                    s.getId()
                });
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao buscar serviço!");
        }
    }//GEN-LAST:event_jbuscarServico1ActionPerformed

    private void jExcluirServico3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jExcluirServico3ActionPerformed
   
        int row = jTable1.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um serviço na tabela!");
            return;
        }

        String id = jTable1.getValueAt(row, 4).toString();

        int confirm = JOptionPane.showConfirmDialog(this,
                "Tem certeza que deseja excluir este serviço?",
                "Confirmar",
                JOptionPane.YES_NO_OPTION);

        if (confirm != JOptionPane.YES_OPTION) return;

        try {
            GestaoServicos.getInstancia().remover(id);
            carregarTodos();
            JOptionPane.showMessageDialog(this, "Serviço excluído!");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao excluir serviço!");
        }
    }//GEN-LAST:event_jExcluirServico3ActionPerformed

    private void jEditarServico2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jEditarServico2ActionPerformed
    
        int linha = jTable1.getSelectedRow();

        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um serviço!");
            return;
        }

        String id = jTable1.getValueAt(linha, 4).toString();
        Servico servico = GestaoServicos.getInstancia().buscarPorId(id);

        EditarServicoPanel painel = new EditarServicoPanel(root);
        painel.setServico(servico);

        root.setSubPainel(painel);
    }//GEN-LAST:event_jEditarServico2ActionPerformed


