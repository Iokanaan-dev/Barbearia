     private void carregarTodos() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        List<Servico> lista = GestaoServicos.getInstancia().getListaCopia();

        for (Servico s : lista) {
            model.addRow(new Object[]{
                s.getNome(),
                s.getPreco(),
                s.getDescricao(),
                s.getTempoEmMinutos(),
                s.getId()
            });
        }
    }  
    
